A = [1 2; 1 -1];
B = [4 -1; 4 -2];
% Operatii ce respecta regulile algebrei liniare
disp('A + B = '); disp(A + B)
disp('A - B = '); disp(A - B)
disp('A * B = '); disp(A * B)
disp('A / B = '); disp(A / B)
disp('A \ B = '); disp(A \ B)
disp('A^2 = '); disp(A ^ 2)
% Operatii element cu element
disp('A .* B = '); disp(A .* B)
disp('A ./ B = '); disp(A ./ B)
disp('A .\ B = '); disp(A .\ B)
